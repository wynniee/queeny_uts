    void cari() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No. Buku");
        tbl.addColumn("Judul Buku");
        tbl.addColumn("Genre Buku");
        tbl.addColumn("Pengarang");
        tbl.addColumn("Penerbit");
        tbl.addColumn("Tahun Terbit");
        tbl.addColumn("Tahun Masuk");
        
        try {
            Statement st = (Statement) Koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM t_perpus1 WHERE no_buku LIKE '%"+txt_cari.getText()+"%'");
            while (rs.next()) {
                tbl.addRow(new Object[]{
                    rs.getString("no_buku"),
                    rs.getString("judul_buku"),
                    rs.getString("genre_buku"),
                    rs.getString("pengarang"),
                    rs.getString("penerbit"),
                    rs.getString("tahun_terbit"),
                    rs.getString("tahun_masuk")
                });
                System.out.println(rs.getString("no_buku"));
                jTable1.setModel(tbl);
            }
//            JOptionPane.showMessageDialog(null, "Koneksi Database Berhasil");
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Koneksi Database Gagal" + e.getMessage());
        }
    }
    
