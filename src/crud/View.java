    private void btn_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_simpanMouseClicked
        // TODO add your handling code here:
        
        int gb = 0;
        
        if(rb_fiksi.isSelected()){
            gb = 1;
        } else if(rb_nonfiksi.isSelected()) {
            gb = 2;
        }
        
        try {
            String sql = "INSERT INTO t_perpus1 (no_buku,judul_buku,genre_buku,pengarang,penerbit,tahun_terbit,tahun_masuk) VALUES('"+this.txt_nobuku.getText()+"',"                    
                    + "'"+this.txt_judul.getText()+"',"
                    + "'"+ gb +"',"
                    + "'"+this.txt_pengarang.getText()+"',"
                    + "'"+this.txt_penerbit.getText()+"',"
                    + "'"+this.txt_tahunterbit.getText()+"',"
                    + "'"+this.txt_tahunmasuk.getText()+"')";
                    Connection oon = (Connection) Koneksi.getConnection();
                    PreparedStatement pst = oon.prepareStatement(sql);
                    pst.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Menyimpan" + e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanMouseClicked

